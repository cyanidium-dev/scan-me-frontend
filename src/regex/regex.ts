export const contactsPhoneRegex =
  /^\+38\s?\(?(\d{3})\)?\s?(\d{3})\s?(\d{2})\s?(\d{2})$/;

// Regex для пароля: мінімум одна велика літера, одна мала літера, одна цифра, один спеціальний символ
// Дозволені спецсимволи: @$!%*?&«»()
export const passwordRegex = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&«»()])[A-Za-z\d@$!%*?&«»()]{6,}$/;
